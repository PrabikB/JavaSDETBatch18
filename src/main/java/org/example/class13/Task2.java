public class Task2 {
    /*Create a method that will take a number and prints whether
    the number is even or odd.

     */

    void evenOrOdd(int num){
        if(num %2==0){
            System.out.println("Even Number");
        }else{
            System.out.println("Odd Number");
        }
    }
}
