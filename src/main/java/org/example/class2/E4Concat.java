public class E4Concat {
    public static void main(String[] args) {
String fName="Bikal";
String lName="Baral";
String space=" ";
        System.out.println(fName+space+lName);
        System.out.println(fName+" "+lName);
        System.out.println("Bikal"+" "+"Baral");
    }
}
