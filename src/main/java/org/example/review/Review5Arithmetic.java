package org.example.review;

public class Review5Arithmetic {
    public static void main(String[] args) {
        int num1=20;
        int num2=10;

        //addition
        System.out.println(num1+num2);
        //subtraction
        System.out.println(num1-num2);
        //multiplication
        System.out.println(num1*num2);
        //division
        System.out.println(num1/num2);
        //modules
        System.out.println(num1%num2);
    }
}
